package com.app.common.utils;

public class MetaDataContent {

	public String english_content;
	public String hindi_content;
	public long uniqueId;
	
	public String getEnglish_content() {
		return english_content;
	}
	public void setEnglish_content(String english_content) {
		this.english_content = english_content;
	}
	public String getHindi_content() {
		return hindi_content;
	}
	public void setHindi_content(String hindi_content) {
		this.hindi_content = hindi_content;
	}
	public long getUniqueId() {
		return uniqueId;
	}
	public void setUniqueId(long uniqueId) {
		this.uniqueId = uniqueId;
	}
}
